    def __init__(self, id_jadwal, stasiun_awal, stasiun_akhir, harga_eko, harga_bis, harga_eks, waktu):
        self.id_jadwal = id_jadwal
        self.stasiun_awal = stasiun_awal
        self.stasiun_akhir = stasiun_akhir
        self.harga_eko = harga_eko
        self.harga_bis = harga_bis
        self.harga_eks = harga_eks
        self.waktu = waktu